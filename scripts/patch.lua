-- Replace old strings with new ones
local oldSetString = _G.TextWidget.SetString
_G.TextWidget.SetString = function(guid, str)
	if type(str) == "string" then
		local origStr = str
        str = STRING_TABLE[str] or str

		-- Hacky workaround to get rid of old strings' lingering characters, by adding spaces to the end
        if #str < #origStr then
            str = str .. string.rep(" ", #origStr - #str)
        end
	end
	oldSetString(guid, str)
end

